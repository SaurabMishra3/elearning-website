<h1>Why Python?</h1><br><br>


Python works on different platforms (Windows, Mac, Linux, Raspberry Pi, etc).<br>
Python has a simple syntax similar to the English language.<br>
Python has syntax that allows developers to write programs with fewer lines than some other programming languages.<br>
Python runs on an interpreter system, meaning that code can be executed as soon as it is written.<br> This means that prototyping can be very quick.<br>
Python can be treated in a procedural way, an object-oriented way or a functional way.<br><br>

